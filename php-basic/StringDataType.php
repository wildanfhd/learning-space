<?php
/** Escape Sequences :
 * \" : Double Quote
 * \\ : Backslash
 * \$ : Membuat dollar sign 
 * \n : enter atau membuat baris baru
 * \t : tab
 */
// Single Quote - Tidak dapat menambahkan atau menggunakan escape sequence
echo 'Nama : ';
echo 'Jang Kyujin';
echo "\n";

//Double Quote - Dapat menambahkan escape sequence seperti \n, \t
echo 'Nama : ';
echo 'Kang Seulgi';
echo "\n";

